print("   1. python 01ingest/01ingest.py --url <URL> --slug <slug>")
print("   2. python 02normalise/02normalise.py --slug <slug>")
print("   3. python 03segment/03segment.py --slug <slug>")
print("   4. python 04names/04names.py")
print("   5. python 07keys/07keys_vdb.py")
print("   6. python 08commentary/08commentary_vdb.py")
print("   7. python 10generate/10generate.py")
print("   8. python 11translate/11translate.py")
print("   9. python 12rebuild/12rebuild.py")
